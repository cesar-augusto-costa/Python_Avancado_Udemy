"""
Operadores de atribuição
= += -= *= /= //= **= %=
NUMERIC
"""

contador = 0

contador += 1
print(contador)

contador += 3
print(contador)

contador -= 1
print(contador)

contador *= 10
print(contador)

contador /= 2
print(contador)

contador //= 2
print(contador)

contador **= 2
print(contador)

contador %= 2
print(contador)
