"""
DocString
Python = Linguagem de programação
Tipo de tipagem = Dinâmica e Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas Simples
print('Luiz "Otávio"')
# Aspas Duplas
print("Luiz 'Otávio'")
# Escape (\)
print("Luiz \"Otávio\"")
# r (RegEx - Expressão Regular)
print(r"Luiz \"Otávio\"")
